#include <avr/io.h>
#include <avr/interrupt.h>
#include <util/delay.h>

void init_ports(void){

    /* -----------------------------------------------------------------
     * | PINB7 | PINB6 | PINB5 | PINB4 | PINB3 | PINB2 | PINB1 | PINB0 |
     * -----------------------------------------------------------------
     * |  N/A  |  N/A  |  SCK  |  MISO |  MOSI |  N/A  | USBEN |  USB4 |
     * -----------------------------------------------------------------
     * |   IN  |   IN  |   IN  |   IN  |   IN  |   IN  |  OUT  |  OUT  |
     * -----------------------------------------------------------------
     */

    DDRB = 0b00000011 & 0x03;
    PORTB = 0b00000010 & 0x02;

    /* -----------------------------------------------------------------
     * |  N/A  | PINC6 | PINC5 | PINC4 | PINC3 | PINC2 | PINC1 | PINC0 |
     * -----------------------------------------------------------------
     * |  N/A  |  N/A  | LED5  | LED4  | LED3  | LED2  | LED1  | P_BTN |
     * -----------------------------------------------------------------
     * |  N/A  |  IN   |  OUT  |  OUT  |  OUT  |  OUT  |  OUT  |  IN   |
     * -----------------------------------------------------------------
     */

    DDRC = 0b00111110 & 0x3E;
    PORTC = 0b00000000 & 0x00;

    /* -----------------------------------------------------------------
     * | PIND7 | PIND6 | PIND5 | PIND4 | PIND3 | PIND2 | PIND1 | PIND0 |
     * -----------------------------------------------------------------
     * |  USB3 |  USB2 |  USB1 |  N/A  |  N/A  |  TXD  |  RXD  | RESET |
     * -----------------------------------------------------------------
     * |  OUT  |  OUT  |  OUT  |  IN   |  IN   |  OUT  |  IN   |  N/A  |
     * -----------------------------------------------------------------
     */

    DDRD = 0b11100100 & 0xE4;
    PORTD = 0b00000000 & 0x00;

    /* PCIE0 => PCINT0..7 PORTB
     * PCIE1 => PCINT8..14 PORTC
     * PCIE2 => PCINT016..23 PORTD
     */

    PCICR = (0 << PCIE0) | (1 << PCIE1) | (0 << PCIE2);

    /*
     * ---------------------------------------------------------------------------------
     * |  PINB7  |  PINB6  |  PINB5  |  PINB4  |  PINB3  |  PINB2  |  PINB1  |  PINB0  |
     * ---------------------------------------------------------------------------------
     * | PCINT7  | PCINT6  | PCINT5  | PCINT4  | PCINT3  | PCINT2  | PCINT1  | PCINT0  |
     * ---------------------------------------------------------------------------------
     */

    PCMSK0 = 0;

    /*
     * ---------------------------------------------------------------------------------
     * |   N/A   |  PINC6  |  PINC5  |  PINC4  |  PINC3  |  PINC2  |  PINC1  |  PINC0  |
     * ---------------------------------------------------------------------------------
     * |         | PCINT14 | PCINT13 | PCINT12 | PCINT11 | PCINT10 | PCINT9  | PCINT8  |
     * ---------------------------------------------------------------------------------
     */

    PCMSK1 = 1 << PCINT8;

    /*
     * ---------------------------------------------------------------------------------
     * |  PIND7  |  PIND6  |  PIND5  |  PIND4  |  PIND3  |  PIND2  |  PIND1  |  PIND0  |
     * ---------------------------------------------------------------------------------
     * | PCINT23 | PCINT22 | PCINT21 | PCINT20 | PCINT19 | PCINT18 | PCINT17 | PCINT16 |
     * ---------------------------------------------------------------------------------
     */

    PCMSK2 = 0;

}

void select_led(int led){

    /* zero LED pins */
    PORTC &= 0b11000001 & 0xC1;

    if(led >= 1 && led <= 5){
        PORTC |= (1 << led);
    }
}

void select_usb(int usb){

    /*         -------------------------------
     *         | su1 | su2 | su3 | su4 | ~OE |
     *         |-----|-----|-----|-----|-----|
     *         | PD5 | PD6 | PD7 | PB0 | PB1 |
     * |-------|-----|-----|-----|-----|-----|
     * | USB-1 |  0  |  0  |  0  |  0  |  0  |
     * |-------|-----|-----|-----|-----|-----|
     * | USB-2 |  1  |  0  |  0  |  0  |  0  |
     * |-------|-----|-----|-----|-----|-----|
     * | USB-3 |  x  |  1  |  0  |  0  |  0  |
     * |-------|-----|-----|-----|-----|-----|
     * | USB-4 |  x  |  x  |  1  |  0  |  0  |
     * |-------|-----|-----|-----|-----|-----|
     * | USB-5 |  x  |  x  |  x  |  1  |  0  |
     * |-------|-----|-----|-----|-----|-----|
     * | NONE  |  x  |  x  |  x  |  x  |  1  |
     * |-------|-----|-----|-----|-----|-----|
     *
     */
    
    /* zero select & OE pins, i.e. ubsb-1 configuration */
    PORTD &= 0b00011111 & 0x1F;
    PORTB &= 0b11111100 & 0xFC;

    switch(usb){

        case 5:
            PORTB |= 1 << 0;
            break;

        case 4:
            PORTD |= 1 << 7;
            break;

        case 3:
            PORTD |= 1 << 6;
            break;

        case 2:
            PORTD |= 1 << 5;
            break;

        case 1:
            /* do nothing */
            break;

        default:
            /* pull OE high i.e. disable all usb multiplexers */
            PORTB |= 1 << 1;
            break;
    }
}

int main(void){
 
    init_ports();

    sei();

    for( ; ; ) asm("nop");

    return 0;
}

ISR(PCINT1_vect){

    static volatile uint8_t count = 0;

    /* select_usb(0) will pull OE high disabling all usb multiplexers */
    select_usb(count % 6);

    /* select_led(0) will disable all led lines */
    select_led(count % 6);

    count++;

    _delay_ms(200);

    PCIFR |= (1 << PCIF1);
}
